    /**
     * 判断一个多边形是否完全包含在一个多多边形内
     * @param multiPolygonWKT 外部多边形（MULTIPOLYGON）的 WKT 字符串
     * @param polygonWKT 内部多边形（POLYGON）的 WKT 字符串
     * @return true 如果内部多边形完全在外部多多边形内，否则为 false
     */
    public static boolean isContains(String multiPolygonWKT, String polygonWKT) {
        // 创建 GeometryFactory 和 WKTReader
        GeometryFactory geometryFactory = new GeometryFactory();
        WKTReader reader = new WKTReader(geometryFactory);

        // 读取 MULTIPOLYGON 和 POLYGON
        MultiPolygon multiPolygon = parseMultiPolygon(reader, multiPolygonWKT);
        MultiPolygon polygon = parseMultiPolygon(reader, polygonWKT);

        // 判断 POLYGON 是否在 MULTIPOLYGON 内
        return multiPolygon.contains(polygon);
    }

    // 辅助方法：解析 MULTIPOLYGON
    private static MultiPolygon parseMultiPolygon(WKTReader reader, String wkt) {
        try {
            return (MultiPolygon) reader.read(wkt);
        } catch (ParseException e) {
            throw new RuntimeException("Failed to parse MultiPolygon: " + wkt, e);
        }
    }
